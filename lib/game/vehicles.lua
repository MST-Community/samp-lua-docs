function vehicles.get_name(id)
    return vehicles.names[id]
end

function vehicles.get_id(name)
    for id, vehicle_name in pairs(vehicles.names) do
        if vehicle_name:lower() == name:lower() then
            return id
        end
    end
    return nil
end

